#!/usr/bin/env node
// Generate a directory "redirect" (`<subpath>/package.json` with `types`/`main` pointing into `lib/`)
// for every declared subpath. Purpose: editor discoverability under the classic (node10) module
// resolution that the standard squid tsconfig uses. There, TypeScript ignores the `exports` map and
// only surfaces a subpath's *children* in autocomplete when the parent is a real, resolvable
// directory — so `@subsquid/squid-sdk/evm/` completes to `rpc`/`fallback` only if `evm/rpc/` and
// `evm/fallback/` exist on disk. Node itself always resolves these subpaths via `exports` at runtime;
// these files are consumed only by editor tooling. Run after `tsc` (see the `build` script).
const fs = require('fs')
const path = require('path')

const pkgDir = path.resolve(__dirname, '..')
const pkg = require(path.join(pkgDir, 'package.json'))

// Stamped into every generated redirect so the cleanup step can tell its own output apart from a
// user-created directory that happens to share a subpath name (`store`, `client`, …).
const MARKER = 'generated by scripts/gen-subpath-stubs.cjs — do not edit'

// A generated redirect is a `package.json` whose only fields are `types`/`main` (both strings) plus our
// optional `//` marker — the shape this script writes. A real package (with `name`/`version`/deps/…)
// fails this, which is the point: it lets us tell our own output apart from a user-created directory.
function isRedirectManifest(p) {
    let json
    try {
        json = JSON.parse(fs.readFileSync(p, 'utf8'))
    } catch {
        return false
    }
    return (
        typeof json.types === 'string' &&
        typeof json.main === 'string' &&
        Object.keys(json).every((k) => k === '//' || k === 'types' || k === 'main')
    )
}

// A directory is safe to delete only if it is, top to bottom, nothing but generated redirects: every
// file is a redirect `package.json` and every subdirectory is itself such a tree. Anything else (a
// stray source file, a real manifest) means it isn't ours — bail rather than wipe it.
function isGeneratedStubTree(dir) {
    for (const entry of fs.readdirSync(dir, {withFileTypes: true})) {
        const p = path.join(dir, entry.name)
        if (entry.isDirectory()) {
            if (!isGeneratedStubTree(p)) return false
        } else if (entry.name === 'package.json') {
            if (!isRedirectManifest(p)) return false
        } else {
            return false
        }
    }
    return true
}

const subpaths = Object.keys(pkg.exports)
    .filter((k) => k !== '.' && k !== './package.json')
    .map((k) => k.replace(/^\.\//, ''))

const topDirs = [...new Set(subpaths.map((s) => s.split('/')[0]))]

// Guard: every generated top-level dir must be in `files`, or publish would silently drop the stubs
// and break discoverability for that subtree.
const missingFromFiles = topDirs.filter((d) => !pkg.files.includes(d))
if (missingFromFiles.length) {
    console.error(`gen-subpath-stubs: add ${missingFromFiles.join(', ')} to "files" in package.json`)
    process.exit(1)
}

// Clean previously generated dirs so rebuilds are idempotent and a removed subpath leaves nothing
// behind — but refuse to touch a same-named directory that isn't ours (see isGeneratedStubTree).
for (const d of topDirs) {
    const dir = path.join(pkgDir, d)
    if (!fs.existsSync(dir)) continue
    if (!isGeneratedStubTree(dir)) {
        console.error(
            `gen-subpath-stubs: refusing to delete ${d}/ — it is not a generated stub tree ` +
                `(contains files other than generated redirects). Remove it manually if this is intended.`,
        )
        process.exit(1)
    }
    fs.rmSync(dir, {recursive: true, force: true})
}

for (const sub of subpaths) {
    const target = path.join(pkgDir, 'lib', sub, 'index.d.ts')
    if (!fs.existsSync(target)) {
        console.error(`gen-subpath-stubs: exports declares "./${sub}" but ${path.relative(pkgDir, target)} is missing — build first`)
        process.exit(1)
    }
    const up = '../'.repeat(sub.split('/').length)
    const dir = path.join(pkgDir, sub)
    fs.mkdirSync(dir, {recursive: true})
    // No `name` field: this is a folder redirect, not a nested package. The `//` marker lets the
    // cleanup step recognise these as generated (Node/TS ignore unknown package.json fields).
    const redirect = {'//': MARKER, types: `${up}lib/${sub}/index.d.ts`, main: `${up}lib/${sub}/index.js`}
    fs.writeFileSync(path.join(dir, 'package.json'), JSON.stringify(redirect, null, 2) + '\n')
}

console.log(`gen-subpath-stubs: wrote ${subpaths.length} subpath redirect(s)`)
